package is.leap.android.aui.ui;

/**
 * This is the enum containing possible Leap Icon states
 * {@link IconState#DISCOVERY}: Trigger UI is visible
 * {@link IconState#NORMAL}: Page or stage searching mode or just simply just showing instruction
 * {@link IconState#DRAGGED}: Dragging on Leap Icon is performed
 */
public enum IconState {
    DISCOVERY, NORMAL, DRAGGED
}
